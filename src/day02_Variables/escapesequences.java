package day02_Variables;

public class escapesequences {
    public static void main(String[] args) {
        System.out.println("Hello World");
        System.out.println("\tHello World");
        // \t gives space to the paragraph
        System.out.println("One Two \nThree");
        // \n print a statement in new line
        System.out.println("\n\n\n Hello Cybertek");
        // \" or \' to put a statment in double or single qoutention
        System.out.println("\'We Trust in GOD\'");
        System.out.println("My favorite TV show is \"Game of Thrones\"");
    }
}

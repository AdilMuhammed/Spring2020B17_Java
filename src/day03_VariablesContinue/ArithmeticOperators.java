package day03_VariablesContinue;

public class ArithmeticOperators {

    public static void main(String[] args) {

        // +, -, *, /, %

        System.out.println(5 + 3);   // 8, addition
        System.out.println(10 - 3);  // 7, subtraction
        System.out.println(10 * 2);  // 20, multiplication
        System.out.println(10 / 2);  // 5, division

          //  System.out.println(9 / 0 ); this gives error coz is denominator cannot be zerro

        System.out.println(10 % 3);  // 1 , it means the remainder of 10/3 is 1
        System.out.println(100 % 33);  // 1, , it means the remainder of 100/33 is 1




    }


}
